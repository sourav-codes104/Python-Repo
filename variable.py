# Variable = A container for a value (string, integer, float, boolean)
# A variable behaves as if it was the value it contains

# Strings
first_name = "Sourav"
print(first_name)
# F- string
print(f"Hello {first_name}")

# Integers
age = 22
quantity = 3
subjects = 4
print(f"You are {age} years old")
print(f"you are buying {quantity} apples!")
print(f"You are currently studying total of {subjects} subjects")

# Float
CGPA = 7.58
distance = 22.5
print(f"Current CGPA is {CGPA}")
print(f"Distance between my college and home is {distance} km")

# Boolean

is_student = True
is_employed = False
print(f"is i am a student ?:{is_student}")
print(f"is i am  employed ?:{is_employed}")

